package Java.Operators;

public class arithmetic {

    public static void main(String[] args) {
        
        //1.Addition
        int num1 = 10;
        int num2 = 12;
        int num3 = num1 + num2;  // (+ Operator)
        System.out.println("1.Addition of two number is:- " + num3);
        
        //2.Subtraction
        int num4 = 12;
        int num5 = 7;
        int num6 = num4 - num5;  // (- Operator)
        System.out.println("2.Subtraction of two number is:- " + num6);

        //3.Multiplication
        int a = 15;
        int b = 2;
        System.out.println("3.Multiplication of two number is:- " + a * b); // (* Operator)
        
        //4.Modulo
        int x = 10;
        int y = 6;
        System.out.println("4.Mod of two number is:- " + x % y); // (% Operator)

        //5.Division
        int s = 12;
        int t = 6;
        System.out.println("5.Division of two number is :- " + s / t); // (/ Operator)

    }
    
}
