package Java.Operators;

public class relational {
    public static void main(String[] args) {
        
        //1.Equal to (==)
        int var1 = 10;
        int var2 = 12;
        System.out.println(var1 == var2); //(==) This means check whether the two given operands are equal or not

        //2.Not Equal to (!=)
        int a = 10;
        int b = 12;
        System.out.println(a != b); //(!=)

        //3.Greater than (>)
        int x = 100;
        int y = 13;
        System.out.println(x > y); //(>)

        //4.Less than (<)
        int s = 10;
        int t = 9;
        System.out.println(s < t); //(<)

        //5.Greater than or equal to (>=)
        int o = 15;
        int p = 15;
        System.out.println(o >= p); //(>=)

        //6.Less than or equal to (<=)
        int c = 12;
        int d = 11;
        System.out.println(c <= d); //(<=)
        
    }
}
